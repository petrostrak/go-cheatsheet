CREATE TABLE person (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    kind varchar(255) NOT NULL,
    persons_name varchar(255) NOT NULL,
    origins varchar(255) NOT NULL,
    programming_languages varchar(255) NOT NULL,
    tools varchar(255) NOT NULL,
    linkedin varchar(255) NOT NULL,
    github varchar(255) NOT NULL,
    personal varchar(255) NOT NULL,
    foreign_languages varchar(255) NOT NULL,
    fav_food varchar(255) NOT NULL,
    fav_drink varchar(255) NOT NULL,
    fav_programming_language varchar(255) Not NULL,
    thinking_about varchar(255) NOT NULL,
    hobbies varchar(255) NOT NULL
);